package com.group.kindergarten.schoolInfo.entity;

public class Phone {
	private int id;
	private String phone;
	public Phone() {
	}
	public Phone(int id, String phone) {
		super();
		this.id = id;
		this.phone = phone;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	
	

}
